export function getBuiltInPromptTemplate(id: string): PromptTemplate | undefined {
  return BUILT_IN_PROMPT_TEMPLATES.find((template) => template.id === id);
}

export function getDefaultPromptTemplate(): PromptTemplate {
  return (
    getBuiltInPromptTemplate(DEFAULT_PROMPT_TEMPLATE_ID) ??
    BUILT_IN_PROMPT_TEMPLATES[0]!
  );
}

export function normalizePromptTemplate(
  value: unknown,
  fallback?: PromptTemplate
): PromptTemplate | null {
  if (!isRecord(value)) {
    return fallback ?? null;
  }

  const id = readString(value.id, fallback?.id ?? "");
  const name = readString(value.name, fallback?.name ?? "");
  const systemPrompt = readString(value.systemPrompt, fallback?.systemPrompt ?? "");

  if (!id.trim() || !name.trim() || !systemPrompt.trim()) {
    return fallback ?? null;
  }

  return {
    id,
    name,
    description: readString(value.description, fallback?.description ?? ""),
    systemPrompt,
    icon: readString(value.icon, fallback?.icon ?? "Sparkles"),
    kind: value.kind === "built_in" ? "built_in" : "custom",
    inputMode: readInputMode(value.inputMode, fallback?.inputMode ?? "single_image")
  };
}

export function isBuiltInPromptTemplateId(id: string): boolean {
  return BUILT_IN_PROMPT_TEMPLATES.some((template) => template.id === id);
}

function readInputMode(
  value: unknown,
  fallback: PromptTemplateInputMode
): PromptTemplateInputMode {
  if (value === "single_image" || value === "multi_image" || value === "text") {
    return value;
  }

  return fallback;
}

function readString(value: unknown, fallback: string): string {
  return typeof value === "string" ? value : fallback;
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}
